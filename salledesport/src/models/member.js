const mongoose = require('mongoose');

const memberSchema = new mongoose.Schema({
  nom: {
    type: String,
    required: true
  },
  prenom: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  }
});

const Member = mongoose.model('Member', memberSchema);

module.exports = Member;
